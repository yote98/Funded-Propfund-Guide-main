// Helper function to get reviews by firm ID
export const getReviewsByFirmId = (firmId: string): UserReview[] => {
  return userReviews.filter(review => review.firmId === firmId);
};

// Helper function to get review statistics
export const getReviewStats = (firmId: string) => {
  const reviews = getReviewsByFirmId(firmId);
  const totalReviews = reviews.length;
  const averageRating = totalReviews > 0 ? reviews.reduce((sum, review) => sum + review.rating, 0) / totalReviews : 0;
  const ratingDistribution = {
    5: reviews.filter(r => r.rating === 5).length,
    4: reviews.filter(r => r.rating === 4).length,
    3: reviews.filter(r => r.rating === 3).length,
    2: reviews.filter(r => r.rating === 2).length,
    1: reviews.filter(r => r.rating === 1).length
  };
  
  return {
    totalReviews,
    averageRating: Math.round(averageRating * 10) / 10,
    ratingDistribution
  };
};
